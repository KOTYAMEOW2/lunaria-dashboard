-- guild-scoped tables
do $$
declare
  tbl text;
begin
  foreach tbl in array array[
    'guild_configs',
    'command_permissions',
    'custom_commands',
    'guild_rules',
    'guild_log_settings',
    'guild_logs',
    'guild_cases',
    'guild_mod_history',
    'temp_bans',
    'temp_roles',
    'ticket_configs',
    'ticket_panels',
    'tickets',
    'voicemaster_configs',
    'voicemaster_rooms',
    'server_customizations'
  ]
  loop
    execute format('drop policy if exists %I_admin_select on public.%I', tbl, tbl);
    execute format('create policy %I_admin_select on public.%I for select to authenticated using (public.is_guild_admin(guild_id))', tbl, tbl);
    execute format('drop policy if exists %I_admin_write on public.%I', tbl, tbl);
    execute format('create policy %I_admin_write on public.%I for all to authenticated using (public.is_guild_admin(guild_id)) with check (public.is_guild_admin(guild_id))', tbl, tbl);
  end loop;
end $$;

