create or replace function public.current_discord_user_id()
returns text
language sql
stable
as $$
  select coalesce(
    auth.jwt() -> 'user_metadata' ->> 'provider_id',
    auth.jwt() -> 'user_metadata' ->> 'sub',
    auth.jwt() -> 'app_metadata' ->> 'provider_id'
  );
$$;

create or replace function public.is_guild_admin(check_guild_id text)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists(
    select 1
    from public.guild_admins ga
    where ga.guild_id = check_guild_id
      and ga.user_id = public.current_discord_user_id()
  );
$$;

